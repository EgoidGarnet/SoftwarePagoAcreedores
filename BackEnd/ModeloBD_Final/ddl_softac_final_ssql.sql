CREATE PROCEDURE [dbo].[pa_sp_reporte_facturas_pendientes]
    @p_acreedor_id INT = NULL,   -- NULL or 0 = sin filtro
    @p_pais_id     INT = NULL,   -- NULL or 0 = sin filtro
    @p_moneda_id   INT = NULL    -- NULL or 0 = sin filtro
AS
BEGIN
    SET NOCOUNT ON;
    
    -- Merge data into PA_REPORTES_FACTURAS_PENDIENTES
    MERGE INTO PA_REPORTES_FACTURAS_PENDIENTES AS target
    USING (
        SELECT
            f.FACTURA_ID,
            a.ACREEDOR_ID,
            pa.PAIS_ID,
            f.MONEDA_ID,
            f.NUMERO_FACTURA,
            a.RAZON_SOCIAL,
            pa.NOMBRE                           AS NOMBRE_PAIS,
            m.CODIGO_ISO                        AS CODIGO_MONEDA,
            f.FECHA_LIMITE_PAGO,
            DATEDIFF(DAY, CAST(GETDATE() AS DATE), f.FECHA_LIMITE_PAGO) AS DIAS_VENCIMIENTO,
            CASE
                WHEN DATEDIFF(DAY, CAST(GETDATE() AS DATE), f.FECHA_LIMITE_PAGO) BETWEEN 0 AND 30  THEN '0-30'
                WHEN DATEDIFF(DAY, CAST(GETDATE() AS DATE), f.FECHA_LIMITE_PAGO) BETWEEN 31 AND 60 THEN '31-60'
                WHEN DATEDIFF(DAY, CAST(GETDATE() AS DATE), f.FECHA_LIMITE_PAGO) BETWEEN 61 AND 90 THEN '61-90'
                ELSE '+90'
            END                                AS RANGO_VENCIMIENTO,
            f.MONTO_RESTANTE
        FROM PA_FACTURAS f
        JOIN PA_ACREEDORES a ON a.ACREEDOR_ID = f.ACREEDOR_ID
        JOIN PA_MONEDAS   m ON m.MONEDA_ID    = f.MONEDA_ID
        JOIN PA_PAISES   pa ON pa.PAIS_ID     = a.PAIS_ID
        WHERE f.ESTADO = 'PENDIENTE'
            AND DATEDIFF(DAY, CAST(GETDATE() AS DATE), f.FECHA_LIMITE_PAGO) >= 0
            AND (@p_acreedor_id IS NULL OR @p_acreedor_id = 0 OR a.ACREEDOR_ID = @p_acreedor_id)
            AND (@p_pais_id IS NULL OR @p_pais_id = 0 OR pa.PAIS_ID = @p_pais_id)
            AND (@p_moneda_id IS NULL OR @p_moneda_id = 0 OR f.MONEDA_ID = @p_moneda_id)
    ) AS source
    ON (target.FACTURA_ID = source.FACTURA_ID AND target.ACREEDOR_ID = source.ACREEDOR_ID)
    WHEN MATCHED THEN
        UPDATE SET
            target.PAIS_ID = source.PAIS_ID,
            target.MONEDA_ID = source.MONEDA_ID,
            target.NUMERO_FACTURA = source.NUMERO_FACTURA,
            target.RAZON_SOCIAL = source.RAZON_SOCIAL,
            target.NOMBRE_PAIS = source.NOMBRE_PAIS,
            target.CODIGO_MONEDA = source.CODIGO_MONEDA,
            target.FECHA_LIMITE_PAGO = source.FECHA_LIMITE_PAGO,
            target.DIAS_VENCIMIENTO = source.DIAS_VENCIMIENTO,
            target.RANGO_VENCIMIENTO = source.RANGO_VENCIMIENTO,
            target.MONTO_RESTANTE = source.MONTO_RESTANTE
    WHEN NOT MATCHED BY TARGET THEN
        INSERT (
            FACTURA_ID,
            ACREEDOR_ID,
            PAIS_ID,
            MONEDA_ID,
            NUMERO_FACTURA,
            RAZON_SOCIAL,
            NOMBRE_PAIS,
            CODIGO_MONEDA,
            FECHA_LIMITE_PAGO,
            DIAS_VENCIMIENTO,
            RANGO_VENCIMIENTO,
            MONTO_RESTANTE
        )
        VALUES (
            source.FACTURA_ID,
            source.ACREEDOR_ID,
            source.PAIS_ID,
            source.MONEDA_ID,
            source.NUMERO_FACTURA,
            source.RAZON_SOCIAL,
            source.NOMBRE_PAIS,
            source.CODIGO_MONEDA,
            source.FECHA_LIMITE_PAGO,
            source.DIAS_VENCIMIENTO,
            source.RANGO_VENCIMIENTO,
            source.MONTO_RESTANTE
        );
END
